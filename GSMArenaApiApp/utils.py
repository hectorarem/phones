def parseSimbolToCoin(simbol):
    if simbol == '&#36;':
        return "USD"
    elif simbol == '&#163;':
        return "GBP"
    elif simbol == '&#8364;':
        return "EUR"
    elif simbol == '&#8377;':
        return "INR"
    else:
        return "MISSED"

